    [RoutePrefix("api/addproductos")]
    public class AddsProductosController : ApiController
    {
        [Route("MarcasProductos/{producto}")]
        public HttpResponseMessage GetMarcaProductos(string producto)
        {
            DataTable table = new DataTable();

            string query = @"select * from MarcasProductos where ProductoMarca='"+producto+"'";

            using (var con = new SqlConnection(ConfigurationManager.ConnectionStrings["Prolapp"].ConnectionString))
            using (var cmd = new SqlCommand(query, con))
            using (var da = new SqlDataAdapter(cmd))
            {
                cmd.CommandType = CommandType.Text;
                da.Fill(table);
            }

            return Request.CreateResponse(HttpStatusCode.OK, table);
        }

